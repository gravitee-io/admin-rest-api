package io.gravitee.rest.api.model.configuration.application;

import java.util.List;

public class ApplicationGrantTypeEntity {

    private String code;

    private String type;

    private String name;

    private List<String> responses_types;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<String> getResponses_types() {
        return responses_types;
    }

    public void setResponses_types(List<String> responses_types) {
        this.responses_types = responses_types;
    }
}
